    /// <summary>
    /// 一个简单的混合线程同步锁，采用了基元用户加基元内核同步构造实现。
    /// 这个 SimpleHybridLock 是一个混合线程同步锁的实现，结合了用户模式的轻量级原子操作和内核模式的事件等待。
    /// 它在低竞争场景下避免昂贵的内核态切换，而在高竞争时使用内核对象实现线程阻塞与唤醒，是一种典型的“混合锁”设计思路。
    /// 实现 IDisposable 接口是为了把资源控制权交给开发者，让开发者能够确定性地、即时地释放重量级系统资源，而不必等待 GC 不确定的回收时机。
    /// </summary>
    internal sealed class SimpleHybridLock : IDisposable
    {
        #region IDisposable Support
        private int _disposeState = 0;  // 检测Dispose方法冗余调用；0 = 未释放，1 = 已释放。
        /// <summary>
        /// 该方法线程安全。
        /// 根据 "所有权链" 原则：如果一个类持有实现了 IDisposable 的成员，且该成员的生命周期与类实例绑定，
        /// 那么这个类也必须实现 IDisposable，并在自己的 Dispose 方法中调用成员的 Dispose。
        /// </summary>
        /// <param name="disposing">是否释放托管对象。该入口参数为true表示该方法由void Dispose()调用，即由程序员调用；
        /// 为false表示该方法由~SimpleHybridLock()调用，即由终结器线程调用。</param>
        private void Dispose(bool disposing)
        {
            if (Interlocked.CompareExchange(ref _disposeState, 1, 0) == 0)    // 原子操作
            {
                // 多线程调用Dispose方法，只有一个线程能执行到这里
                if (disposing)
                {
                    // TODO:释放托管状态(托管对象)。可能有助于程序员控制提前释放资源。
                }

                // 没有必要在下面将托管资源关闭、清理或置空，在一些情况下有安全隐患是一点，托管资源的释放依赖于根引用，这里虽置空
                // 但是其它地方可能还有引用，而且相应所有的终结器执行完毕解除引用后，相关对象将在下一次 GC 中被回收，
                // 届时，内存便会释放，没有必要在下面额外将成员置空。
                // TODO:释放未托管的资源(未托管的对象)并在以下内容中替代终结器。（最主要的目的！！！）
                //（GC可以自动释放托管堆上申请的内存，但不能自动释放内核句柄（非托管资源）！）
                // TODO:将大型字段设置为null。可能有助于控制提前释放大资源。
                if (m_waiterLock != null)
                {
                    // m_waiterLock 虽然是一个托管对象（WaitHandle的派生类），但它的 Close 方法内部操作的是内核句柄（非托管资源）。
                    // WaitHandle.Close() 最终调用的是其内部 SafeHandle 的 Dispose()。
                    // SafeHandle 内部维护了一个引用计数和原子状态位。如果该句柄已被关闭（计数归零或状态置为 Closed），
                    // 再次调用 Close() 会被 SafeHandle 拦截并静默忽略，不会尝试二次关闭同一个内核对象。
                    //（不拥有非托管资源的对象的Close方法可能不使用SafeHandle，这样二次关闭就会有安全隐患。）
                    //---------------------------------------------------------------------------------------------------------------------
                    // 提问：对象A持有对象B（组合关系），对象A存活，对象B也必须存活吗？
                    // 回答：GC判断对象是否存活（即不被回收）的算法是图遍历（Graph Traversal）：
                    // 1. 从一组根（Roots）出发：包括线程栈上的局部变量、静态字段、CPU寄存器、以及fReachable队列（存放等待终结的对象）。
                    // 2. 递归访问每一个被根引用的对象。
                    // 3. 凡是能通过引用链到达的对象，都被标记为存活，本次 GC 绝对不回收。
                    // 结论：如果 A 存活（是根，或被根引用），那么 A 引用的 B 也会被 GC 视为存活。这是组合关系的必然结果。
                    //（但是，访问已经执行过终结器的对象是有安全隐患的。）
                    //---------------------------------------------------------------------------------------------------------------------
                    // 提问：那么，对象A持有对象B（组合关系），对象B存活，则对象A不一定存活吗？
                    // 回答：是的，对象 B 存活，对象 A 不一定存活。
                    // 这是因为.NET GC 的可达性分析是单向的——从根出发，沿着引用链向下标记。
                    // B 被标记为存活，只能说明至少有一个根能够到达 B，但这个根不一定经过 A。
                    // 所以，在这个情况：对象A持有对象B的引用（组合关系），对象A没有定义终结器，对象B定义了终结器，
                    // 就会发生对象B所占内存比对象A所占内存晚释放。
                    m_waiterLock.Close();    // 释放非托管资源：关闭内核句柄；等效于 m_waiterLock.Dispose()。
                    //GC.SuppressFinalize(m_waiterLock);
                    m_waiterLock = null;
                }
            }
        }

        // TODO:仅当以上 Dispose(bool disposing) 拥有用于释放未托管资源的代码时才替代终结器。
        // 终结器的调用方：GC的终结器线程（在对象被垃圾回收时）。
        //（为何需要独立的终结线程？：使用独立线程主要是为了保证GC的性能与稳定性。如果在GC线程中直接执行用户编写的终结代码，一旦代码存在死循环或耗时操作（如数据库访问），就会阻塞整个GC进程，导致内存无法回收。）
        //-------------------------------------------------------------------------------------------------------------------------------------
        // 当一个包含终结器（即C#中的析构函数）的对象在托管堆上被创建时，CLR（公共语言运行时）会将该对象的指针放入终结队列。
        // 当GC运行时，会检查终结队列中的对象指针。一旦发现某个对象已不可达（即成为垃圾），GC会将该对象的指针从终结队列移除，并转移到FReachable队列中。这个过程被称为对象的“复活”（Resurrection），因为它暂时免于被回收。
        // FReachable队列是等待执行终结方法的“工作队列”。CLR有一个独立的高优先级专用线程（终结线程），它会不断监控这个队列。一旦队列中有条目，终结线程就会被唤醒，并依次调用队列中每个对象的Finalize方法。
        //-------------------------------------------------------------------------------------------------------------------------------------
        // 当 Dispose(bool disposing) 拥有用于释放未托管资源的代码且拥有该未托管资源的成员拥有终结器时（AutoResetEvent类内部定义了终结器），
        // 不定义本类的终结器。因为当SimpleHybridLock对象和AutoResetEvent成员对象都被标记为垃圾时，若都定义了终结器，则
        // GC的终结器线程会执行这两个终结器，而这两个终结器都会释放同一个未托管资源，这也许会造成问题。
        // （对象A内部持有一个成员对象B（聚合/组合关系），它们终结器的调用顺序是不确定的。）
        //-------------------------------------------------------------------------------------------------------------------------------------
        // 当用户不显式调用Dispose方法时，对象m_waiterLock所占内存可能在本对象所占内存释放了后才被下一次GC释放，
        // 因为对象m_waiterLock有定义终结器，GC在第一次扫描时不会立即释放它的内存，而是会将它放入一个名为“f-reachable”的特殊队列中，
        // 等待终结器线程调用它的终结器；在终结器执行完成后，对象m_waiterLock所占的内存在下一次GC中才被真正释放。
        //-------------------------------------------------------------------------------------------------------------------------------------
        // 当用户显式调用Dispose方法时，m_waiterLock.Close()最后会调用GC.SuppressFinalize(this)以抑制m_waiterLock对象的终结器，
        // 当m_waiterLock对象的终结器被成功抑制后，GC对该对象的第一次回收（即它变为不可达后的第一次 GC）就可以立即释放其所占内存。
        //~SimpleHybridLock()
        //{
        //    // 请勿更改此代码。将清理代码放入以上 Dispose(bool disposing) 中。
        //    Dispose(false);
        //}

        // 添加此代码以正确实现可处置模式。
        /// <summary>
        /// 释放资源。Dispose方法由开发者显式调用，而非由GC调用。该方法线程安全。
        /// </summary>
        public void Dispose()
        {
            // 请勿更改此代码。将清理代码放入以上 Dispose(bool disposing) 中。
            Dispose(true);
            // TODO:如果在以上内容中替代了终结器，则取消注释以下行。
            //GC.SuppressFinalize(this);    // 告诉垃圾回收器（GC）不要调用本对象的终结器（在当前对象的对象头（Object Header）中设置一个特定的比特位（bit）标记）。
        }
        #endregion

        /// <summary>
        /// 基元用户模式构造同步锁。m_waiters字段记录当前等待获取锁+已经获取锁的线程总数。
        /// </summary>
        private Int32 m_waiters = 0;

        /// <summary>
        /// 基元内核模式构造同步锁。
        /// 托管堆上的 AutoResetEvent 对象本身所占用的内存由垃圾回收器（GC）管理。
        /// 但它内部持有的内核句柄属于非托管资源，GC 并不知道如何释放它（GC 仅回收托管内存）。
        /// 因此 AutoResetEvent 自身实现了 IDisposable 接口，并通过终结器（Finalizer）作为兜底机制。
        /// </summary>
        private AutoResetEvent m_waiterLock = new AutoResetEvent(false);

        /// <summary>
        /// 获取锁。
        /// 无竞争时：仅执行一次原子递增操作，性能极高。
        /// 有竞争时：线程进入内核等待队列，CPU不会空转消耗资源。
        /// </summary>
        public void Enter()
        {
            // 原子递增 m_waiters，并返回递增后的值。
            if (Interlocked.Increment(ref m_waiters) == 1)
            {
                // 当用户锁可以使用的时候，直接返回，在第一次调用时发生。
                // 情况A：递增前 m_waiters == 0 → 当前没有线程持有或等待锁，
                // 则当前线程成功获得锁，直接返回，无需内核态操作。
                return;
            }
            // 当发生锁竞争时，使用内核同步构造锁。
            // 情况B：已有其他线程持有锁或正在等待 → 发生竞争，
            // 则当前线程在内核事件上阻塞，等待被唤醒。
            m_waiterLock.WaitOne();
        }

        /// <summary>
        /// 离开锁。
        /// </summary>
        public void Leave()
        {
            // 原子递减 m_waiters，并返回递减后的值。
            // 正常情况下，递减后的值就是当前正在等待的线程数；但当Enter方法的m_waiterLock.WaitOne()调用抛出了异常
            // 并且捕获了后，这里递减后的值就是当前正在等待的线程数加上取得了锁正在执行的线程数之和。
            if (Interlocked.Decrement(ref m_waiters) == 0)
            {
                // 情况A：递减后为 0 → 没有其他线程在等待，
                // 则直接返回，无需唤醒操作。
                return;
            }
            // 情况B：仍有其他线程在等待 → 唤醒其中一个。
            // Set()会将 AutoResetEvent 置为有信号状态，并自动复位，恰好唤醒一个等待线程（如果有的话）。
            m_waiterLock.Set();
        }

        /// <summary>
        /// 获取当前是否有正在等待获取锁或已经获取锁的线程，是则为true。
        /// Int32 类型的读写在 .NET 运行时中保证是原子的（CLI 规范要求 32 位及以下数据类型的读写为原子操作）。
        /// 如果该属性被用于无任何其他同步的低延迟自旋等待（例如 while (!lock.IsWaiting) { /* spin */ }），
        /// 则可能需要 Volatile.Read(ref m_waiters) 来强制刷新缓存，否则编译器和 CPU 可能将读取优化到循环外导致死循环。
        /// </summary>
        public bool IsWaiting => m_waiters != 0;
    }
